package ExercicioDeAula;

public class TiposNumericos {
	public static void main(String[] args) {
		Byte numeroByte = -128; // -128 ate 127
		
		Short variavelShort = 1; //-32768 ate 32767
		
		Integer variavelInterger = 1; // -2147483648 ate 2147486347
		
		Long variavelLong = 1L; // -9,223372037×10 18 ate 9,223372037×10 18
		
		Float variavelFloat = 1.0f;
		
		Double variavelDouble = 1.0;
		
		Character variavelCharacter = 'A';
		
		Boolean variavelBoolean = true;
		
		
		System.out.println("numeroByte: " + numeroByte + "\nnumeroInterger: " + variavelInterger);
	
	}
}
